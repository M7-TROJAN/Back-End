/*
In C#, we can initialize an array during the declaration. For example,
    int [] numbers = {1, 2, 3, 4, 5};

Here, we have created an array named 'numbers' and initialized it with values '1, 2, 3, 4, and 5' inside the curly braces.

Note that we have not provided the size of the array. 
In this case, the C# automatically specifies the size by counting the number of elements in the array (i.e. 5).

In an array, we use an index number to determine the position of each array element. 
We can use the index number to initialize an array in C#. For example,

declare an array:
    int[] age = new int[5];

initializing array:
    age[0] = 12;
    age[1] = 4;
    age[2] = 5;
    ...


Note:
    An array index always starts at 0. That is, the first element of an array is at index 0.
    If the size of an array is 5, the index of the last element will be at 4 (5 - 1).
*/

